								<h1 class="text-color-dark font-weight-bold">Concept</h1>
								<p class="font-weight-medium h6 line-height-5">CherrySeRB is basically oriented towards the field of agro-biodiversity, aiming to add value to unexplored Serbian fruit genetic resources through new protocols and approaches for their genotypic and phenotypic characterisation. Two fruit species of worldwide importance are targeted − diploid sweet cherry and tetraploid sour cherry. The main idea is connection of the production challenges arising from climate change and demands for quality and healthy fruits, and the richness of Serbian cherry germplasm that is still unused enough. Characterisation of the indigenous genotype potential for desirable reproductive properties and defining ʻgood reproductive behaviour cherry modelʼ that can face warmer temperature conditions during the flowering are in focus. CherrySeRB provides S-genotyping and the choice of suitable pollenisers that maximize fertilization efficacy for landraces, as well as their nutritional/phytochemical profiles that enable obtaining of new high-added value nutraceutical products.</p>
								<p class="text-3-5" style="color: #000;font-weight: 600;">Representing novelty in breeding approach that can be applicable to other fruit species, the ultimate goal of CherrySeRB is to foster the use of Serbian cherry germplasm as the material adapted to climate change in order to contribute to sustainable fruit production and ensure the availability of healthy food for the future.</p>
								
