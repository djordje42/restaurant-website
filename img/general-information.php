							<h1 class="text-color-dark font-weight-bold">Objectives</h1>
							<p class="font-weight-medium text-4-5 line-height-5">CherrySeRB aims to add value to genetic resources of the Rosaceae family fruit tree species. It enables better genotypic and phenotypic characterisation, as well as use and exchange of enhanced autochthonous diversity of two economically important fruit species in the Republic of Serbia and worldwide ‒ sweet and sour cherry, in order to address challenges of the climate change and cherry-fruit sector. Particular attention has paid on genotypes that characterise the traits associated with reproductive adaptability and fruit quality, but also nutritional value and contribution to human health.</p>
							<h4>The specific objectives of the CherrySeRB project are to: </h4>	
							<ul>
								<li><b>Determine gametophytic self-(in)compatibility (GSI) status of selected autochthonous cherry genotypes</b> through detailed molecular analysis of S-haplotypes, pollen tubes growth monitoring and fruit set assessment after self-pollination. </li>
								<li><b>Provide suitable polleniser(s) for autochthonous cherry genotypes</b> by pollinating them with a series of pollenisers chosen according to genetic compatibility, flowering time overlap, commercial value and breeding potential.</li>
								<li><b>Characterise the potential of autochthonous cherry genotypes for desirable reproductive properties and define ʻgood reproductive behaviour cherry modelʼ that can face the climate change challenge</b> through the investigation of reproductive behaviour from the aspect of ovule longevity and pollen performance in relation to temperature impact on open-, cross- and non-pollinated flowers, in both field and laboratory (controlled temperature) conditions.</li>
								<li><b>Define the potential of autochthonous cherry genotypes for fruit quality, nutritional and health-promoting compounds</b> by analysing fruit quality parameters, nutritional and phytochemical compounds, and antioxidant potential from the aspects of: i) using the genotypes in current/future breeding work; ii) successful commercial cherry growing and obtaining premium-quality fruits suitable for fresh consumption, industrial and domestic processing. </li>
								<li><b>Promote the use of Serbian autochthonous sweet and sour cherry genotypes at international and national level.</b> Autochthonous cherry genotypes previously selected due to favourable biological properties (ripening time; fruit size; tolerance to causal agents of cherry diseases), will be additionally characterised from the aspects of GSI, suitable pollenisers that can increase production efficacy, adaptable reproductive behaviour in conditions of higher flowering temperatures, and content of nutritional and health-related compounds. </li>
							</ul>
